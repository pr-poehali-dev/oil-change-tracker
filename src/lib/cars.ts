// ─── Типы ─────────────────────────────────────────────────────────
export type ManualStep = {
  step: number;
  title: string;
  items: string[];
  warning?: string;
};
export type ManualGuide = { id: string; title: string; icon: string; steps: ManualStep[]; article?: string; interval?: string; };
export type Consumable = {
  id: string;
  category: string;
  name: string;
  spec: string;
  article: string;
  interval: string;
  qty: string;
  note: string;
};

export type ServiceInterval = {
  id: string;
  name: string;
  icon: string;
  color: string;
  interval_km: number | null;
  interval_months: number | null;
  unit: "km" | "months";
  last_km?: number;       // пробег при последней замене
  last_date?: string;     // дата последней замены ISO
};

